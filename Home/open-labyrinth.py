from operator import sub, add


def checkio(maze_map):
    end = (10, 10)
    start = (1, 1)
    dirs = ((-1, 0), (0, -1), (1, 0), (0, 1))
    dirname = 'NWSE'
    prev = dict()

    QSIZE = 144
    q = [0] * 144
    head, tail = 0, 0
    q[head] = start
    head = (head + 1) % QSIZE
    ans = []
    while not head == tail:
        cur = q[tail]
        tail = (tail + 1) % QSIZE
        if cur == end:
            while cur != start:
                ans.append(dirname[prev[cur]])
                cur = tuple(map(sub, cur, dirs[prev[cur]]))
            return ''.join(ans[::-1])
        for i, d in enumerate(dirs):
            nxt = tuple(map(add, cur, d))
            if maze_map[nxt[0]][nxt[1]] == 0:
                maze_map[nxt[0]][nxt[1]] = 1
                q[head] = nxt
                head = (head + 1) % QSIZE
                prev[nxt] = i


